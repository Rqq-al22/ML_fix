"""### Menentukan Klaster Optimal menggunakan Metode Elbow (K-Means)

Untuk menemukan jumlah klaster (k) yang optimal untuk K-Means, kita akan menggunakan metode Elbow. Metode ini melibatkan perhitungan *Within-Cluster Sum of Squares* (WCSS) untuk nilai k yang berbeda. WCSS mengukur jumlah jarak kuadrat antara setiap titik dan centroid dalam sebuah klaster.

Kita membuat plot WCSS terhadap jumlah klaster (k). Titik 'siku' pada plot, di mana laju penurunan WCSS melambat secara signifikan, sering dianggap sebagai k yang optimal.
"""

wcss = []
k_range = range(2, 11) # K from 2 to 10

for k in k_range:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10) # Set n_init to suppress warning
    kmeans.fit(df_scaled)
    wcss.append(kmeans.inertia_)

# Plotting the Elbow Method
plt.figure(figsize=(10, 6))
plt.plot(k_range, wcss, marker='o', linestyle='--')
plt.title('Elbow Method for Optimal K')
plt.xlabel('Number of Clusters (K)')
plt.ylabel('Within-Cluster Sum of Squares (WCSS)')
plt.xticks(k_range)
plt.grid(True)
plt.show()

"""### Skor Silhouette untuk K Optimal (K-Means)

Selain metode Elbow, kita bisa menggunakan Skor Silhouette untuk menentukan jumlah klaster optimal. Skor Silhouette adalah ukuran seberapa mirip suatu objek dengan klaster sendiri (kohesi) dibandingkan dengan klaster tetangga lainnya (pemisahan).

*   Skor mendekati +1 menunjukkan bahwa objek sangat cocok dengan klaster sendiri dan kurang cocok dengan klaster tetangga.
*   Skor mendekati 0 menunjukkan bahwa objek berada di atau sangat dekat dengan batas keputusan antara dua klaster tetangga.
*   Skor mendekati -1 menunjukkan bahwa objek mungkin telah salah diklasifikasikan ke klaster yang salah.
"""

from sklearn.metrics import silhouette_score

silhouette_scores = []

for k in k_range:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    cluster_labels = kmeans.fit_predict(df_scaled)
    silhouette_avg = silhouette_score(df_scaled, cluster_labels)
    silhouette_scores.append(silhouette_avg)

# Plotting the Silhouette Scores
plt.figure(figsize=(10, 6))
plt.plot(k_range, silhouette_scores, marker='o', linestyle='--')
plt.title('Silhouette Scores for Optimal K')
plt.xlabel('Number of Clusters (K)')
plt.ylabel('Silhouette Score')
plt.xticks(k_range)
plt.grid(True)
plt.show()

# Display the scores
for k, score in zip(k_range, silhouette_scores):
    print(f"For K = {k}, Silhouette Score = {score:.4f}")

