df['cluster_agglomerative'] = best_agg_clustering.fit_predict(df_selected)

print(f"Agglomerative Clustering applied with K={best_k} and linkage='{best_linkage}'.")
print("\nFirst 5 rows of the dataset with Agglomerative cluster labels:")
display(df.head())

# Prepare data for PCA visualization
