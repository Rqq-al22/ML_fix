"""### Klastering DBSCAN

Sekarang, mari kita jelajahi DBSCAN (*Density-Based Spatial Clustering of Applications with Noise*). DBSCAN adalah algoritma klastering non-parametrik berbasis densitas yang dapat menemukan klaster dengan bentuk arbitrer dan mendeteksi titik noise. Algoritma ini membutuhkan dua parameter utama:

*   **`eps` (epsilon):** Jarak maksimum antara dua sampel agar salah satunya dianggap berada di lingkungan yang lain.
*   **`min_samples`:** Jumlah sampel (atau total bobot) dalam lingkungan agar sebuah titik dianggap sebagai titik inti. Ini termasuk titik itu sendiri.

Kita akan menguji berbagai kombinasi `eps` dan `min_samples` untuk menemukan konfigurasi terbaik berdasarkan Skor Silhouette. Perlu dicatat bahwa titik-titik yang diklasifikasikan sebagai *noise* oleh DBSCAN (label klaster -1) tidak akan dimasukkan dalam perhitungan Skor Silhouette.
"""

from sklearn.cluster import DBSCAN
from sklearn.metrics import silhouette_score
import numpy as np

# Define parameter ranges to test
eps_range = np.arange(0.3, 1.6, 0.1) # from 0.3 to 1.5
min_samples_range = range(3, 11) # from 3 to 10

# Dictionary to store silhouette scores for each combination
dbscan_silhouette_results = {}

best_dbscan_score = -1
best_dbscan_eps = -1
best_dbscan_min_samples = -1

# Iterate through eps and min_samples
for eps in eps_range:
    for min_samples in min_samples_range:
        dbscan = DBSCAN(eps=eps, min_samples=min_samples)
        cluster_labels = dbscan.fit_predict(df_scaled)

        # Filter out noise points for Silhouette Score calculation
        unique_labels = set(cluster_labels)
        if -1 in unique_labels: # Remove noise if present
            unique_labels.remove(-1)

        # Only calculate Silhouette Score if more than one cluster is found (excluding noise)
        if len(unique_labels) > 1:
            # Get indices of non-noise points
            non_noise_indices = cluster_labels != -1
            score = silhouette_score(df_scaled[non_noise_indices], cluster_labels[non_noise_indices])

            dbscan_silhouette_results[(eps, min_samples)] = score

            if score > best_dbscan_score:
                best_dbscan_score = score
                best_dbscan_eps = eps
                best_dbscan_min_samples = min_samples

# Print the results
print("Silhouette Scores for DBSCAN Clustering (eps, min_samples):")
for (eps, min_samples), score in dbscan_silhouette_results.items():
    print(f"  eps = {eps:.1f}, min_samples = {min_samples}, Silhouette Score = {score:.4f}")

print(f"\nBest DBSCAN Model:")
print(f"  Optimal eps = {best_dbscan_eps:.1f}")
print(f"  Optimal min_samples = {best_dbscan_min_samples}")
print(f"  Highest Silhouette Score = {best_dbscan_score:.4f}")

