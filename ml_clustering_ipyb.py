"""### 2 **Klastering Aglomeratif**
Sekarang, mari kita jelajahi Klastering Aglomeratif, yang merupakan jenis klastering hierarkis. Kita akan menguji berbagai jumlah klaster (dari 2 hingga 10) dan berbagai metode linkage ('ward', 'complete', 'average') untuk menemukan konfigurasi terbaik berdasarkan Skor Silhouette.
"""

from sklearn.metrics import silhouette_score
from sklearn.cluster import AgglomerativeClustering
import numpy as np

# Define the range of clusters and linkage methods to test
k_range = range(2, 11) # K from 2 to 10
linkage_methods = ['ward', 'complete', 'average']

# Dictionary to store silhouette scores for each combination
silhouette_results = {}

# Iterate through linkage methods and number of clusters
for linkage in linkage_methods:
    scores_for_linkage = []
    for k in k_range:
        # 'ward' linkage only works with Euclidean distance and n_clusters > 1
        if linkage == 'ward' and k == 1:
            continue

        # Apply Agglomerative Clustering
        agg_clustering = AgglomerativeClustering(n_clusters=k, linkage=linkage)
        cluster_labels = agg_clustering.fit_predict(df_scaled)

        # Calculate Silhouette Score
        score = silhouette_score(df_scaled, cluster_labels)
        scores_for_linkage.append(score)
    silhouette_results[linkage] = scores_for_linkage

# Print the results
print("Silhouette Scores for Agglomerative Clustering:")
for linkage, scores in silhouette_results.items():
    print(f"Linkage Method: {linkage}")
    for i, score in enumerate(scores):
        print(f"  K = {k_range[i]}, Silhouette Score = {score:.4f}")

# Find the best parameters
best_score = -1
best_k = -1
best_linkage = ''

for linkage, scores in silhouette_results.items():
    for i, score in enumerate(scores):
        if score > best_score:
            best_score = score
            best_k = k_range[i]
            best_linkage = linkage

print(f"\nBest Agglomerative Clustering Model:")
print(f"  Optimal K = {best_k}")
print(f"  Optimal Linkage = {best_linkage}")
print(f"  Highest Silhouette Score = {best_score:.4f}")

"""### Menerapkan Model Klastering Aglomeratif Terbaik dan Visualisasi

Berdasarkan Skor Silhouette, kita sekarang akan menerapkan model Klastering Aglomeratif terbaik dan memvisualisasikan hasilnya menggunakan PCA.
"""

# Apply Agglomerative Clustering with the best parameters
